def create_ctrl(crv_type,
                crv_name=None):
    if not crv_name:
        crv_name: str = crv_type

    if crv_type == "Circle":
        curve_transform = cmds.circle(name=crv_name, radius=1)[0]

    else:
        curve_transform: str = cmds.curve(name=crv_name, p=shapes_data[crv_type]["points"],
                                          per=False, d=shapes_data[crv_type]["degree"],
                                          k=shapes_data[crv_type]["knots"])

    curve_shapes: list = cmds.listRelatives(curve_transform, shapes=1)
    for shape in curve_shapes:
        cmds.rename(shape, f"{curve_transform}Shape")

    cmds.select(d=1)
    return curve_transform


def change_shape_with_connection(ctrls_list,
                                 new_ctrl):
    for ctrl in ctrls_list:
        source_shapes = cmds.listRelatives(ctrl, shapes=True)

        new_transform = cmds.duplicate(new_ctrl)
        pos = cmds.xform(ctrl, q=True, ws=True, t=True)

        if pos == (0, 0, 0):
            cmds.xform(new_transform, ws=True, t=pos, ztp=True)
            cmds.makeIdentity(new_transform, apply=True)

        new_shapes = cmds.listRelatives(new_transform, shapes=True)

        cmds.parent(new_shapes, ctrl, relative=True, shape=True)
        cmds.delete(new_transform)

        for source_shape, new_shape in zip(source_shapes, new_shapes):
            all_connections = cmds.listConnections(source_shape, source=True, scn=True, et=True, p=True)

            if all_connections:
                shape_connection_source = all_connections[-1]
                connection_dest_list = cmds.listConnections(shape_connection_source,
                                                            destination=True, scn=True, et=True, p=True)

                for shape_dest in connection_dest_list:
                    if shape_dest.split(".")[0] in source_shapes:
                        shape_obj_attr = shape_dest.split(".")[-1]
                        cmds.connectAttr(shape_connection_source, new_shape + "." + shape_obj_attr, force=True)

        cmds.delete(source_shapes)
        for shape in cmds.listRelatives(ctrl, shapes=True):
            cmds.rename(shape, ctrl + "Shape")

    cmds.delete(new_ctrl)
    cmds.select(d=1)


@core.enable_undo
def create_replace_shape(ctrl_shape,
                         replace=False,
                         create=True):
    ctrls = cmds.ls(sl=1)
    if replace:
        new_ctrl = create_ctrl(ctrl_shape, crv_name=None)
        change_shape_with_connection(ctrls, new_ctrl)
    elif create:
        create_ctrl(ctrl_shape, crv_name=None)
    else:
        print("Choose Replace or Create")

    cmds.select(d=1)


def mirror_along_specific_plane(crv_to_mirror,
                                output_mirror_to=None,
                                negate_axis=None):
    #
    if negate_axis is None:
        negate_axis = ["+X", "+Y", "+Z"]

    cvs: int = core.get_curve_shape_info(output_mirror_to)
    cords: [float] = core.get_curve_points_cords(crv_to_mirror)

    # For Mirrored/Non-Mirrored Transforms SET The Negation To what Fit Axis right
    for index in range(cvs):
        x: float = cords[index][0] if negate_axis[0] == "+X" else -cords[index][0]
        y: float = cords[index][1] if negate_axis[1] == "+Y" else -cords[index][1]
        z: float = cords[index][2] if negate_axis[2] == "+Z" else -cords[index][2]

        cmds.xform(f"{output_mirror_to}.cv[{index}]", t=[x, y, z])


def rotate_curve_shape(axe):
    sel = cmds.ls(sl=1, fl=1)
    for each in sel:
        cmds.select('{}.cv[*]'.format(each))
        if axe == 'X':
            cmds.rotate(90, 1, 1, os=True)
        elif axe == 'Y':
            cmds.rotate(1, 90, 1, os=True)
        elif axe == 'Z':
            cmds.rotate(1, 1, 90, os=True)
    cmds.select(sel)


